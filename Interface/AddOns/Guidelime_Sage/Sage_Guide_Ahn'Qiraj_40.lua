[OC]    C'thun
[OC]====================================

[OC]Pull:
       [OC]Everyone: Mount up before the encounter starts
       [OC]Everyone: One player with high Nature Resist will soak the first Eye Beam
       [OC]Everyone: Stagger groups running to their zone every 2 seconds
       [OC]Everyone: Soaker, then 1/2, then 3/4, then 5/6, then 7/8
       [OC]Everyone: Stay 10 yards from other players and stay in your slice
[OC]Phase 1 (C'thun's Eye):
       [OC]Kill Order: Claw Tentacle > Eye Tentacle > C'Thun
       [OC]Green Beam Eye Beam: Stay 10 yards from other players and in your group zone
       [OC]Red Beam Dark Glare: Rotate 180 degrees around C'Thun and DPS as hard as possible
[OC]Phase 2 (C'thun):
       [OC]Kill Order: Giant Claw Tentacle > Giant Eye Tentacle > Claw Tentacle > Eye Tentacle > C'Thun
       [OC]Everyone: Entire raid should collapse to the stairs
       [OC]Everyone: When stomach tentacles die C'thun is vulnerable for 45 seconds
       [OC]DPS: DPS as hard as possible when C'thun is vulnerable
       [OC]Tanks: Tank Claw Tentacle when they spawn
[OC]Phase 2 (C'thun's Stomach):
       [OC]Tanks: Run to the stone circle to teleport back to the raid immediately
