[OC]    Azuregos
[OC]====================================
[OC]Everyone: Flank Azuregos because dragon.
[OC]Everyone: Stay within 30 yards of Azuregos because Teleport.
[OC]Everyone: Let tank gain aggro after Teleport.
[OC]Everyone: Disperse after Teleport because Manastorm.
[OC]Everyone: If you die you can't re-engage for 15m because Mark of Frost.
[OC]Tanks: OT can stand more than 30 yards away to ensure aggro stays on a tank after Teleport.
[OC]Caster: Avoid Reflect.
[OC]Mana Users: If you OOM, drop combat and drink.


